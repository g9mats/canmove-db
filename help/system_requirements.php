<h4><b>System Requirements</b></h4>
<p>
The CAnMove Database interface is based on the web standards so any browser should be sufficient to use the system.
</p>
<p>
There are however subtle differences among browsers.
The recommended browser is Chrome since that is the browser that has been used to develop the system.
The ambition has been to check that all functionality used is supported by all the major browsers.
Besides Chrome that is Internet Explorer / Edge, Firefox, Safari and Opera.
Because time has been limited this check has been performed by consulting the reference pages at 
<a href="https://www.w3schools.com/" target="_blank">w3schools.com</a>
rather than testing all the code in all the browsers.
</p>
