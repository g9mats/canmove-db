<h4><b>CAnMove Account</b></h4>
<p>
Anyone can have a look at the meta data which means the descriptions of the datasets. E.g. title, owner, how many animals and tracks, type of data, method, site, dates and contact person.
</p><p>
To create and manage datasets with meta data and actual movement data you need a CAnMove account. Use contact link below to apply for an account.
</p><p>
For employees at Lund University the CAnMove account will as default be connected to the LUCAT authentication database using CAS (Central Authentication Service) software. When you click on the "Log in"-button you will be asked to provide your LUCAT identity and password.
</p><p>
If you do not have a LUCAT identity your CAnMove account will be set up for local authentication.
To login you must click on "Cancel CAS login" and then you will instead be asked to provide your CAnMove identity and password.
</p>
