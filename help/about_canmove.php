<h4><b>About CAnMove</b></h4>
<p>
The CAnMove Database is a national infrastructure for storage of animal movement data of various types.
</p>
<p>
The database with interface for data owners and curators is hosted at Lund University.
For Open Access availability data is streamed to the Analysis Portal of
<a href="http://www.slu.se/en/subweb/swedish-lifewatch/">Swedish LifeWatch</a>
via
<a href="http://www.slu.se/en/Collaborative-Centres-and-Projects/wireless-remote-animal-monitoring-wram/">WRAM</a>.
</p>
