<h4><b>Nano Biology Lab Data Structure</b></h4>
<p>
In the Nano Biology Lab small animals are colour marked with nano dots, held in a water tank and filmed to register their movements in three dimensions.
</p><p>
The films are analysed by software and the result is stored in a single text file containing all the information on recordings, files, animals, tracks, trackpoints and the setup of the experiment.
So this module has only one data subset.
The structure is rather fixed with the exception of the setup information and occasionally there may be additional animal information.
