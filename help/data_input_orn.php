<h4><b>Ornithodolite Data Input</b></h4>
<p>
Data is loaded in the only existing data subset: Trackpoints.
</p><p>
Since the output from the Ornithodolite equipment is completely controlled by the PC software and the loading routine is adjusted to that format there is no need for any manual preparation of the text files.
</p><p>
When the loading routine encounters a balloon tracking a wind profile will be created automatically so there is no need to load them separately.
</p><p>
When uploading the file to the file server archive it must be tagged as final.
</p>
