<h4><b>Ornithodolite Data Structure</b></h4>
<p>
The Ornithodolite is in principal a pair of binoculars attached to a PC with sensors that registers direction, elevation and distance to a flying target.
</p><p>
The output is a text file in a fixed format that is completely controlled by the PC software.
Each file contains a session object with e.g. information on location, position, altitude and air pressure followed by a number of tracks with trackpoints expressed in vector lengths in three dimensions.
Some of the tracked objects are weather balloons for the purpose of creating wind profiles which is used to adjust living animals flight efforts.
