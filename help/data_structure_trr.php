<h4><b>Tracking Radar Data Structure</b></h4>
<p>
This is a military tracking radar connected to a PC that can register direction, elevation and distance to a flying target.
</p><p>
The PC stores the information directly into a database.
Some of the tracked objects are weather balloons for the purpose of creating wind profiles which is used to adjust living animals flight efforts.
The equipment can also detect wing beat frequences.
